package main

import (
	"net/http"

	"github.com/gin-gonic/contrib/static"
	"github.com/gin-gonic/gin"
)

func main() {
	route := gin.Default()

	route.Use(static.Serve("/", static.LocalFile("./frontend/build", true)))

	api := route.Group("/api")
	{
		api.GET("/", func(c *gin.Context) {
			c.JSON(http.StatusOK, gin.H{
				"message": "pong",
			})
		})
	}

	route.Run(":5000")
}
